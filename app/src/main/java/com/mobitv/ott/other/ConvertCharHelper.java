    public static String convertVN(String text){
        char[] array = text.toCharArray();
        for(int i = 0; i < array.length; i++){
            for(int j = 0; j < aCoDau.length; j++){
                if(array[i] == aCoDau[j]){
                    array[i] = bKhongDau[j];
                }
            }
        }
        return String.valueOf(array);
    }
